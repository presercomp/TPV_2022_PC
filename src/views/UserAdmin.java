    public void listarUsuarios(){
        Usuarios u = new Usuarios();
        ResultSet registros = u.result();  
        System.out.println(this.tableData.getRowCount());
        if(this.tableData.getRowCount() > 0){
            while(this.tableData.getRowCount() > 0){
                System.out.println(this.tableData.getRowCount());
                ((DefaultTableModel) this.tableData.getModel()).removeRow(0);
            }
        }
        try {            
            while(registros.next()){
                Object[] row = new Object[2];
                row[0] = registros.getInt(1);
                row[1] = registros.getString(2);                
                ((DefaultTableModel) this.tableData.getModel())
                        .insertRow(registros.getRow()-1, row);
            }
        } catch(SQLException ex) {
            System.out.println("Cago:"+ex.getMessage());
        }
    }

